#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	a;

	if (!s1 || !s2)
		return (0);
	a = 0;
	while (a < n && s1[a] && s1[a] == s2[a])
		a++;
	if (a == n || s1[a] == s2[a])
		return (1);
	return (0);
}
