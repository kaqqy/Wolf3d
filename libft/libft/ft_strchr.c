#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		a;

	if (!s)
		return (NULL);
	a = 0;
	while (s[a] && s[a] != (char)c)
		a++;
	if (s[a] == (char)c)
		return ((char*)(s + a));
	return (NULL);
}
