#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t	a;

	if (!s1 || !s2)
		return (0);
	a = 0;
	while (s1[a] && s1[a] == s2[a])
		a++;
	if (s1[a] == s2[a])
		return (1);
	return (0);
}
