#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	a;

	a = ft_strlen(s);
	while (a && s[a] != (char)c)
		a--;
	if (s[a] == (char)c)
		return ((char*)(s + a));
	return (NULL);
}
