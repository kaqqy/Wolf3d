#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	len1;
	size_t	len2;

	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	s1[len1 + len2] = '\0';
	while (len2--)
		s1[len1 + len2] = s2[len2];
	return (s1);
}
