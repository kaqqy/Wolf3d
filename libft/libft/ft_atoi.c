#include "libft.h"

int		ft_atoi(const char *str)
{
	int		sign;
	int		out;
	int		a;

	a = 0;
	while (str[a] == 32 || (str[a] > 8 && str[a] < 14))
		a++;
	sign = str[a] == '-' ? -1 : 1;
	a += (str[a] == '+' || str[a] == '-') ? 1 : 0;
	out = 0;
	while (str[a] >= '0' && str[a] <= '9')
		out = out * 10 + (str[a++] - '0') * sign;
	return (out);
}
